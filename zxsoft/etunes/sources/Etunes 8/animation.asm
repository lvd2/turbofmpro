Animation_init:
		xor	a
		ld	(Animation_step),a
		ld	(Animation_phase),a
Animation_view:
		ld	a,(Animation_step)
		inc	a
		and	07h
		ld	(Animation_step),a
		ret	nz
		ld	hl,43CCh
		ld	a,(Animation_phase)
		inc	a	
		cp	10h
		jr	c,Animation_next_phase
		xor	a
Animation_next_phase:
		ld	(Animation_phase),a

		ex	de,hl
		ld	l,a
		ld	h,0
		add	hl,hl
		ld	bc,Animation_table_phase
		add	hl,bc
		ld	a,(hl)
		inc	hl
		ld	h,(hl)
		ld	l,a
		ex	de,hl

		ld	b,58				;размерность по Y
Animation_loop_Y:
		push	hl
		ld	a,(de)
		ld	(hl),a
		inc	de
		inc	l
		ld	a,(de)
		ld	(hl),a
		inc	de
		inc	l
		ld	a,(de)
		ld	(hl),a
		inc	de
		inc	l
		ld	a,(de)
		ld	(hl),a
		inc	de
		inc	l
		ld	a,(de)
		ld	(hl),a
		inc	de
		inc	l
		ld	a,(de)
		ld	(hl),a
		inc	de
		inc	l
		ld	a,(de)
		ld	(hl),a
		inc	de
		pop	hl
		inc	h
		ld	a,h
		and 	7
		jr	nz,Animation_next_line
		ld	a,l
		add	20h
		ld	l,a
		jr	c,Animation_next_line
		ld	a,h
		sub	8
		ld	h,a
Animation_next_line:
    		djnz    Animation_loop_Y
		ret	    

